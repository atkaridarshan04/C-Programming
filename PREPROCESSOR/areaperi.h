#ifndef AREAPERI_H
#define AREAPERI_H

#define TRIANGLE_AREA(base, height) (0.5 * (base) * (height))
#define TRIANGLE_PERIMETER(side1, side2, side3) ((side1) + (side2) + (side3))

#define SQUARE_AREA(side) ((side) * (side))
#define SQUARE_PERIMETER(side) (4 * (side))

#define CIRCLE_AREA(radius) (3.14159265359 * (radius) * (radius))
#define CIRCLE_PERIMETER(radius) (2 * 3.14159265359 * (radius))

#endif
