// sizeof operator

#include<stdio.h>

int main()
{
    printf("The size of int here is %d bytes\n",sizeof(int));
    printf("The size of float here is %d bytes\n",sizeof(float));
    printf("The size of char here is %d bytes\n",sizeof(char));
    printf("The size of double here is %d bytes\n",sizeof(double));

    return 0;
}